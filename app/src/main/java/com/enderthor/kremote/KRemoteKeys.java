package com.enderthor.kremote;

/**
 * Represents a button in Remote.
 */
public enum KRemoteKeys {

    /**
     * Right key.
     * MENU_DOWN in ANT
     */
    RIGHT,

    /**
     * Left key.
     * LAP in ANT
     */
    BACK,

    /**
     * Middle up key
     * UNRECOGNIZED in ANT
     */
    MIDDLE
}